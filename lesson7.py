class Matrix:
    def __init__(self, matrix):
        self.matrix = matrix

    def __str__(self):
        for i in self.matrix:
            for j in i:
                print(j, end=' ')
            print('')
        return ""  # этот "костыль" необходим, чтобы __str__ возвращал тип str

    def __add__(self, other):
        result = Matrix([])
        for i in range(len(self.matrix)):
            row = []
            for j in range(len(self.matrix[i])):
                row.append(self.matrix[i][j] + other.matrix[i][j])
            result.matrix.append(row)
        return result


m1 = Matrix([[1, 2, 3], [4, 5, 6]])
m2 = Matrix([[6, 5, 4], [3, 2, 1]])

print(m1)  # 1-й экземпляр матрицы
print(m2)  # 2-й экземпляр матрицы
print(m1 + m2)  # сложение m1 и m2 (их сумма - тоже объект класса Matrix)

