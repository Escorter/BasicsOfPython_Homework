class Cell:
    def __init__(self, n):
        self.n = n

    def __add__(self, other):
        result = Cell(0)
        result.n = self.n + other.n
        return result

    def __sub__(self, other):
        result = Cell(0)
        if self.n - other.n <= 0:
            print("Разность количества ядер клеток должна быть > 0")
            result.n = None
        else:
            result.n = self.n - other.n
        return result

    def __mul__(self, other):
        result = Cell(1)
        result.n = self.n * other.n
        return result

    def __floordiv__(self, other):
        result = Cell(0)
        result.n = self.n // other.n
        return result

    def make_order(self, row_capacity):
        result = ''
        if row_capacity > self.n:
            result = "Кол-во ядер в ряду д.б. < или = кол-ву ядер в клетке"
        else:
            rows = self.n // row_capacity
            rest = self.n % row_capacity
            for i in range(rows):
                for j in range(row_capacity):
                    result += "*"
                result += "\n"
            result += rest * "*"
        return result


c1 = Cell(2)
c2 = Cell(5)
c3 = Cell(10)
