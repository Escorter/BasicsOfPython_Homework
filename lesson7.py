from abc import ABC, abstractmethod


class Clothes(ABC):
    @abstractmethod
    def __init__(self):
        pass

    @abstractmethod
    def consumption(self):
        pass


class Coat(Clothes):
    def __init__(self, x):
        self.size = x

    @property
    def consumption(self):
        return self.size / 6.5 + 0.5


class Suit(Clothes):
    def __init__(self, x):
        self.height = x

    @property
    def consumption(self):
        return self.height * 2 + 0.3


c = Coat(13)
print(c.consumption)  # расход ткани на пальто

s = Suit(5)
print(s.consumption)  # расход ткани на костюм

